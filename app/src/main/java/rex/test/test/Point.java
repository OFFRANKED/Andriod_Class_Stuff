public class Point {
    int m_X;
    int m_Y;

    public Point(int x, int y)
    {
        this.m_X = x;
        this.m_Y = y;
    }

    public int getM_X()
    {
        return m_X;
    }

    public int getM_Y()
    {
        return m_Y;
    }
}
